    {
        $data = $request->only('first_name', 'last_name', 'username', 'phone_number', 'email', 'password');
        $data['password'] = bcrypt($data['password']);
        $user = User::create($data);
        if ($user) {
            return "Successfully Registered";
        }

        return back()->withInputs();
    }

